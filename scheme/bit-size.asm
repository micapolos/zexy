  ifndef BitSize_asm
  define BitSize_asm
    include ../zexy.asm

    _module BitSize
      ; ------------------------------------------------------
      ; Input:
      ;   A - bit size, 0..15
      ; Output:
      ;   DE - size
      ; =========================================================
      _proc GetSize
        ld      hl, table
        rlca
        add     hl, a
        ldi     de, (hl)
      _end

      _data table
        dw      %0000000000000010
        dw      %0000000000000100
        dw      %0000000000001000
        dw      %0000000000010000
        dw      %0000000000100000
        dw      %0000000001000000
        dw      %0000000010000000
        dw      %0000000100000000
        dw      %0000001000000000
        dw      %0000010000000000
        dw      %0000100000000000
        dw      %0001000000000000
        dw      %0010000000000000
        dw      %0100000000000000
        dw      %1000000000000000
        dw      %0000000000000000
      _end
    _end
  endif
